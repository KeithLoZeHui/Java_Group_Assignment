    private List<String[]> readRegisteredUsers() {
        List<String[]> users = new ArrayList<>();
        try (BufferedReader br = new BufferedReader(new FileReader("registered_users.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(",");
                if (data.length >= 3) {
                    users.add(new String[]{data[0], data[2], data[3]}); // Only store email, username, and password
                }
            }
            
        } catch (IOException e) {
            e.printStackTrace();
        }
        return users;
    }
    
